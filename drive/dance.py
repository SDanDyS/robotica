    def sidetoside(self):
        self.motor1.forward()
        self.motor2.backwards()
        time.sleep(1)
        self.motor2.forward()
        self.motor1.backwards()
        time.sleep(1)
        
    def switch(self):
        self.motor1.forward()
        self.motor2.backwards()
        time.sleep(2)
        self.motor1.forward()
